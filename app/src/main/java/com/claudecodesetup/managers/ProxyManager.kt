package com.claudecodesetup.managers

import android.util.Log
import kotlinx.coroutines.Dispatchers
import kotlinx.coroutines.delay
import kotlinx.coroutines.withContext
import okhttp3.OkHttpClient
import okhttp3.Request
import java.io.File
import java.util.concurrent.TimeUnit

class ProxyManager(private val envManager: EnvironmentManager) {

    private val TAG = "ProxyManager"
    private var proxyProcess: Process? = null

    private val pingClient = OkHttpClient.Builder()
        .connectTimeout(2, TimeUnit.SECONDS)
        .readTimeout(2, TimeUnit.SECONDS)
        .build()

    suspend fun startProxy(onStatus: (String) -> Unit): Boolean = withContext(Dispatchers.IO) {
        killProxy()

        val workDir = File(envManager.homeDir, "free-claude-code-main")
        if (!workDir.exists()) {
            onStatus("Proxy files missing")
            return@withContext false
        }

        val proxyCommand = buildProxyCommand(workDir)

        try {
            val pb = ProcessBuilder(proxyCommand).apply {
                directory(workDir)
                redirectErrorStream(true)
                environment().apply {
                    put("HOME", envManager.homeDir.absolutePath)
                    put("PATH", "${envManager.termuxPrefix.absolutePath}/bin:" +
                        "${envManager.homeDir.absolutePath}/node-v20.11.0-linux-arm64/bin:" +
                        "/usr/local/bin:/usr/bin:/bin")
                }
            }

            proxyProcess = pb.start()
            onStatus("Starting proxy...")

            // Poll until proxy is up or timeout
            repeat(15) { i ->
                delay(1000)
                if (isProxyAlive()) {
                    Log.i(TAG, "Proxy ready after ${i + 1}s")
                    return@withContext true
                }
            }

            onStatus("Proxy timeout")
            false
        } catch (e: Exception) {
            Log.e(TAG, "Proxy start failed", e)
            onStatus("Proxy failed: ${e.message}")
            false
        }
    }

    private fun buildProxyCommand(workDir: File): List<String> {
        val bashBin = envManager.termuxPrefix.resolve("bin/bash").absolutePath
        val uvBin = "/root/.local/bin/uv"

        return listOf(
            bashBin, "-c",
            "proot-distro login ubuntu -- /bin/bash -c " +
            "'cd ~/free-claude-code-main && " +
            "kill \$(lsof -t -i:8082) 2>/dev/null; " +
            "$uvBin run uvicorn server:app --host 0.0.0.0 --port 8082'"
        )
    }

    fun isProxyAlive(): Boolean {
        return try {
            val req = Request.Builder().url("http://localhost:8082/").get().build()
            val resp = pingClient.newCall(req).execute()
            resp.isSuccessful || resp.code in 400..499
        } catch (e: Exception) {
            false
        }
    }

    fun killProxy() {
        proxyProcess?.destroy()
        proxyProcess = null

        // Also kill by port inside Ubuntu
        try {
            val bash = envManager.termuxPrefix.resolve("bin/bash").absolutePath
            ProcessBuilder(bash, "-c",
                "proot-distro login ubuntu -- /bin/bash -c 'kill \$(lsof -t -i:8082) 2>/dev/null'"
            ).start().waitFor()
        } catch (e: Exception) {
            // Best effort
        }
    }

    fun writeEnvFile(
        providerId: String,
        apiKey: String,
        modelId: String,
        baseUrl: String
    ) {
        val envFile = File(envManager.homeDir, "free-claude-code-main/.env")
        envFile.parentFile?.mkdirs()
        envFile.writeText(buildEnvContents(providerId, apiKey, modelId, baseUrl))
    }

    private fun buildEnvContents(
        providerId: String,
        apiKey: String,
        modelId: String,
        baseUrl: String
    ): String = buildString {
        appendLine("# Auto-generated by ClaudeCode Setup")
        appendLine("API_PROVIDER=$providerId")
        appendLine("API_KEY=$apiKey")
        appendLine("MODEL=$modelId")
        appendLine("BASE_URL=$baseUrl")
        appendLine("REQUEST_TIMEOUT=120")
        appendLine("CONNECT_TIMEOUT=30")
        appendLine("HOST=0.0.0.0")
        appendLine("PORT=8082")
    }
}
